    void init() {
        setIconImage(ShareHelper.APP_ICON);
        setLocationRelativeTo(null);
        setTitle("Quản lý nhân viên");
        updateStatus();
        fillTable();
        clearForm();
        setDefaultCloseOperation(2);
    }

    void fillTable() {
        DefaultTableModel mol = (DefaultTableModel) tblNhanvien.getModel();
        mol.setRowCount(0);
        try {
            List<Nhanvien> list = dao.selectAll();
            for (Nhanvien nv : list) {
                Object[] row = {
                    nv.getMaNV(),
                    nv.getMatKhau(),
                    nv.getHoTen(),
                    nv.isVaiTro() ? "Trưởng phòng" : "Nhân viên"
                };
                mol.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu");
            e.printStackTrace();
        }
    }

    void edit() {
        try {
            String maNV = (String) tblNhanvien.getValueAt(this.row, 0);
            Nhanvien model = dao.selectById(maNV);
            if (model != null) {
                setForm(model);
                tabs.setSelectedIndex(0);
                updateStatus();
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "lỗi truy vấn dữ liệu");
        }
    }

    void setForm(Nhanvien model) {
        txtMaNV.setText(model.getMaNV());
        txtHoTen.setText(model.getHoTen());
        txtMatKhau.setText(model.getMatKhau());
        txtXacNhanMK.setText(model.getMatKhau());
        rdoTruongPhong.setSelected(model.isVaiTro());
        rdoNhanVien.setSelected(!model.isVaiTro());
    }

    Nhanvien getForm() {
        Nhanvien nv = new Nhanvien();
        nv.setMaNV(txtMaNV.getText());
        nv.setHoTen(txtHoTen.getText());
        nv.setMatKhau(new String(txtMatKhau.getPassword()));
        nv.setVaiTro(rdoTruongPhong.isSelected());
        return nv;
    }

    void clearForm() {
        Nhanvien nv = new Nhanvien();
        setForm(nv);
        row = -1;
        updateStatus();
    }

    void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanvien.getRowCount() - 1);

        txtMaNV.setEditable(!edit);
        btnInsert.setEnabled(!edit);
        btnUpdate.setEnabled(edit);
        btnDelete.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPrev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void insert() {
        Nhanvien nv = getForm();
        String mk2 = new String(txtMatKhau.getPassword());
        if (!mk2.equals(nv.getMatKhau())) {
            DialogHelper.alert(this, "xác nhận mật khẩu không đúng !");
        } else if (txtHoTen.getText().length() == 0 || txtMaNV.getText().length() == 0 || txtMatKhau.getPassword().length == 0 || txtXacNhanMK.getPassword().length == 0) {
            DialogHelper.alert(this, "Không được bỏ trống");
            return;
        } else {
            try {
                dao.insert(nv);
                this.fillTable();
                this.clearForm();
                DialogHelper.alert(this, "Thêm mới thành công");
            } catch (Exception e) {
                e.printStackTrace();
                DialogHelper.alert(this, e.toString());
            }
        }
    }

    void update() {
        Nhanvien nv = getForm();
        String mk2 = new String(txtMatKhau.getPassword());
        if (!mk2.equals(nv.getMatKhau())) {
            DialogHelper.alert(this, "xác nhận mật khẩu không đúng");
        } else {
            try {
                dao.update(nv);
                this.fillTable();
                clearForm();
                DialogHelper.alert(this, "Cập nhật thành công");
            } catch (Exception e) {
                DialogHelper.alert(this, "Cập nhật thất bại");
            }
        }
    }

