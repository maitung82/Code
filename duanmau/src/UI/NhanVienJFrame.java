    void delete() {
        if (!Auth.isManager()) {
            DialogHelper.alert(this, "Bạn không có quyền xóa nhân viên");
        } else {
            String maNv = txtMaNV.getText();
            if (maNv.equals(Auth.user.getMaNV())) {
                DialogHelper.alert(this, "Bạn không thể xóa chính bạn");
            } else if (DialogHelper.confirm(this, "Bạn có chắc muốn xóa nhân viên này?")) {
                try {
                    dao.delete(maNv);
                    this.fillTable();
                    this.clearForm();
                    DialogHelper.alert(this, "Xóa thành công");
                } catch (Exception e) {
                    DialogHelper.alert(this, "Không thể xóa nhân viên đang công tác");
                }
            }
        }

    }

    void firt() {
        row = 0;
        edit();
    }

    void pre() {
        if (this.row > 0) {
            row--;
            edit();
        }
    }

    void next() {
        if (this.row < tblNhanvien.getRowCount() - 1) {
            this.row++;
            edit();
        }
    }

    void last() {
        row = tblNhanvien.getRowCount() - 1;
        edit();
    }

